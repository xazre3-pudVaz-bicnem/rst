// ============================================================
// 営業ターゲット判定（共通）。ターゲットは「個人事業主・5人以下の小規模店」。
// 大手企業・チェーン・フランチャイズ・公共施設・大型商業施設・道の駅/産直/JA等は営業対象外（EXCLUDED）。
// 全ソース（Google Places / 地域メディア / Instagram / 連番探索）で使う。
// ============================================================

// 公共・大型・第三セクター・大企業 系（営業適性が低い＝個人事業主ではない）。
// 注意: 地名に紛れる語（公園/神社/神宮/学校/会館/ＳＡ等）は誤検出するため入れない。店名に出る明確な語のみ。
const BIG_OR_PUBLIC_RE = new RegExp(
  [
    // 道の駅・産直・農漁協・市場（第三セクター/組合・小規模個人店ではない）
    '道の駅', '産地直売', '産直市場', '産直[セ館]', '直売所', '青果市場', '卸売市場', '公設市場', '中央市場',
    'ＪＡ[ぁ-んァ-ヶ一-龥]', '農業協同組合', '漁業協同組合', '漁協', '生活協同組合', '生協', 'コープ[ぁ-んァ-ヶ]', 'ファーマーズマーケット',
    // 行政・公共（明確に施設名）
    '市役所', '区役所', '町役場', '村役場', '都庁', '県庁', '合同庁舎', '公民館', '図書館', '総合体育館',
    // 大型商業施設・量販（テナント募集側＝個人店ではない）
    'イオンモール', 'イオンタウン', 'イオンスタイル', 'アリオ', 'ららぽーと', 'イトーヨーカドー', 'ヨーカドー',
    'ショッピングセンター', 'ショッピングモール', 'アウトレットモール', 'ＰＡＲＣＯ', 'ルミネ', 'アトレ',
    'ドン・キホーテ', 'コストコ', 'ホームセンター', '家電量販', '量販店', 'スーパーセンター', 'メガドンキ',
    'サービスエリア', 'パーキングエリア',
    // 大規模法人・教育・医療（明確語）
    '総合病院', '大学病院', '医療センター', '独立行政法人', '社会福祉法人', '医療法人[^\\s]{0,8}病院',
    'ホールディングス', '株式会社[^\\s]{0,8}グループ本社',
  ].join('|'),
)

/** 大手/公共/大型施設/道の駅/産直/JA等か（＝個人事業主の小規模店ではない）。
 *  店名（＋短い住所）に対して使うこと。本文/スニペットを渡すと地名で誤検出するので渡さない。 */
export function detectBigOrPublic(text: string): { exclude: boolean; reason: string; hit: string } {
  const m = String(text || '').match(BIG_OR_PUBLIC_RE)
  if (m) return { exclude: true, hit: m[0], reason: `大手/公共/大型施設（${m[0]}）のため営業対象外。ターゲットは個人事業主・5人以下の小規模店。` }
  return { exclude: false, reason: '', hit: '' }
}

// 絶対に誤検出しない強語のみ（本文/タイトル/プロフィールにも使える）。道の駅・直売所・大型商業施設など。
const STRONG_BIG_RE = /(道の駅|産地直売|直売所|ファーマーズマーケット|イオンモール|イオンタウン|アリオ|ららぽーと|イトーヨーカドー|ショッピングモール|ショッピングセンター|アウトレットモール|コストコ|ドン・キホーテ|ホームセンター)/
export function detectBigOrPublicStrong(text: string): { exclude: boolean; reason: string; hit: string } {
  const m = String(text || '').match(STRONG_BIG_RE)
  if (m) return { exclude: true, hit: m[0], reason: `大手/公共/大型施設（${m[0]}）のため営業対象外。ターゲットは個人事業主・5人以下の小規模店。` }
  return { exclude: false, reason: '', hit: '' }
}

// 個人事業主・小規模店っぽい加点シグナル（屋号・個人店の語）。HOTを底上げするわけではないが、参考フラグ。
const SMALL_BIZ_RE = /(個人|こだわり|自家製|手作り|アトリエ|工房|専門店|サロン|隠れ家|テイクアウト|キッチンカー|間借り|週末|不定休|店主|オーナー|ハンドメイド|プライベートサロン|完全予約制|自宅サロン|一軒家)/
export function looksSmallBiz(text: string): boolean {
  return SMALL_BIZ_RE.test(String(text || ''))
}

// 多店舗展開・フランチャイズ・チェーン本部（＝確立済み大型。個人事業主・小規模店ではない）。
// 店名/プロフィール/本文に出る明確な語のみ（地名で誤検出しない）。
const MULTI_STORE_RE = /(全国[0-9０-９]{1,4}\s*店舗|[0-9０-９]{1,4}\s*店舗(展開|運営|を展開|を運営|チェーン)|[0-9０-９]{2,4}\s*号店|全国展開|チェーン展開|フランチャイズ|ＦＣ募集|FC募集|加盟店募集|加盟店募集中|直営[0-9０-９]{1,3}\s*店|グループ[0-9０-９]{1,3}\s*店|多店舗展開|店舗拡大中|全国に[0-9０-９]{1,4})/
/** 多店舗展開/フランチャイズ/チェーン本部か（確立済み大型＝営業対象外）。名前＋プロフィール/本文に使える（誤検出しない強語のみ）。 */
export function detectMultiStore(text: string): { exclude: boolean; reason: string; hit: string } {
  const m = String(text || '').match(MULTI_STORE_RE)
  if (m) return { exclude: true, hit: m[0], reason: `多店舗展開/フランチャイズ（${m[0].trim()}）のため営業対象外。ターゲットは個人事業主・小規模店。` }
  return { exclude: false, reason: '', hit: '' }
}

