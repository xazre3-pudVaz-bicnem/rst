  'マックスバリュ', 'イオンモール', 'イオンタウン', 'イオンスタイル', 'まいばすけっと', 'ダイエー', 'マルエツ', 'ライフコーポレーション', 'ヨークマート', 'ベイシア', 'ヤオコー', 'サミットストア',
  'ウエルシア', 'スギ薬局', 'マツモトキヨシ', 'マツキヨ', 'ココカラファイン', 'ツルハ', 'サンドラッグ', 'クスリのアオキ', 'コスモス薬品',
  'セブンイレブン', 'セブン-イレブン', 'ファミリーマート', 'ローソン', 'ミニストップ', 'デイリーヤマザキ',
  'ニトリ', '無印良品', '業務スーパー', 'ドン・キホーテ', 'ドンキホーテ', 'カインズ', 'コーナン', 'コメリ', 'ケーヨー', 'ヤマダ電機', 'ヤマダデンキ', 'ビックカメラ', 'ヨドバシ', 'エディオン', 'ケーズデンキ', 'ユニクロ', 'ジーユー',
  // 大手飲食チェーン
  'リンガーハット', 'すき家', '吉野家', '松屋フーズ', 'ガスト', 'バーミヤン', 'ジョナサン', 'サイゼリヤ', 'コメダ珈琲', 'コメダ', 'スターバックス', 'スタバ', 'ドトール', 'タリーズ', 'マクドナルド', 'マック', 'ケンタッキー', 'モスバーガー', '丸亀製麺', '大戸屋', 'やよい軒', 'ほっともっと', 'ココイチ', 'カレーハウスCoCo', '日高屋', '幸楽苑', 'リンガス',
