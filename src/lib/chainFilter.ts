// 鉄道/交通/観光グループ（大手）→ EXCLUDED
const RAIL_GROUP = ['小田急', '東急', '京王', '西武鉄道', '西武百貨店', '西武園', '阪急', '阪神', '近鉄', '名鉄', '東武', '京急', '京成', 'JR東日本', 'JR西日本', 'JR東海', 'JR九州', 'JR北海道', 'JR四国']

// 英数字だけの語（GEO/AOKI/LAVA/RIZAP等）は単語境界を付ける。iフラグ＋境界なしだと部分一致で
// George→GEO / Aoki→AOKI / Lavanda→LAVA のように個人店を大手チェーン扱いで恒久EXCLUDEDにしてしまう。
const LARGE_RE = new RegExp(
  `(${[...LARGE_CHAIN, ...RAIL_GROUP]
    .map((s) => {
      const esc = s.replace(/[.*+?^${}()|[\]\\]/g, '\\$&')
      return /^[A-Za-z0-9]+$/.test(s) ? `\\b${esc}\\b` : esc
    })
    .join('|')})`,
  'i',
)
// 鉄道大手名＋グループ/ホテル/百貨店 等は確実に大手グループ
const RAIL_PREFIX_RE = /(小田急|東急|京王|西武|阪急|阪神|近鉄|名鉄|東武|京急|京成|JR)[^\s]{0,8}(グループ|ホールディングス|ホテル|百貨店|不動産|電鉄|箱根|リゾート|興業|商事)/

// チェーン/大手の疑い → HOLD（確定ではない）
const SUSPECT_RE = /(グループ|ホールディングス|ホールディング|ＨＤ|\bHD\b|株式会社[^\s]{1,10}(?:グループ|ホールディングス)|(?:東海|関東|関西|九州|北海道|東北|中部|北陸|中国|四国|首都圏)(?:支社|支店|営業所|カンパニー)|リテール|フーズ株式会社|[^\s]{2,10}(?:本部|本社|支店|営業所|カンパニー)|[A-Za-z]{2,}\s?ジャパン|[^\s]{2,10}センター(?:店)?$)/

export interface ChainResult { definite: boolean; suspect: boolean; label: string }

/** 店名（必要なら記事タイトル/本文も）からチェーン/大手を判定 */
export function detectChain(name: string, extra?: string): ChainResult {
  const n = String(name || '')
  const hay = `${n} ${extra || ''}`
  // 店名そのものに大手チェーン名 → 確定除外
  if (LARGE_RE.test(n)) {
    const m = (n.match(LARGE_RE) || [])[1] || ''
    return { definite: true, suspect: true, label: `大手チェーン/グループ会社として検出（${m}）` }
  }
  // 本文/タイトル(extra)にだけ出る大手チェーン名は「セブンイレブンの向かい」「スタバから徒歩3分」等の
  // 目印記述であることが大半。確定除外にすると個人店が恒久EXCLUDEDになるため、疑い(=HOLD)に留める。
  if (extra && LARGE_RE.test(hay)) {
    const m = (hay.match(LARGE_RE) || [])[1] || ''
    return { definite: false, suspect: true, label: `本文に大手チェーン名（${m}）＝目印記述の可能性のため手動確認` }
  }
  if (RAIL_PREFIX_RE.test(hay)) return { definite: true, suspect: true, label: '鉄道/交通系グループ会社として検出' }
  if (SUSPECT_RE.test(n)) {
    const m = (n.match(SUSPECT_RE) || [])[0] || ''
    return { definite: false, suspect: true, label: `チェーン/大手の疑い（${m}）のため手動確認` }
  }
  return { definite: false, suspect: false, label: '' }
}
